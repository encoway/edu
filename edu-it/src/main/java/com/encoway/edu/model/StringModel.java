package com.encoway.edu.model;

/**
 * {@link DemoModel} for {@link String}.
 */
public class StringModel extends DemoModel<String> {

    /**
     * Initializes a {@link StringModel}.
     *
     * @param name the name
     * @param value the value
     */
    public StringModel(String name, String value) {
        super(name, value);
    }

    @Override
    public String getValue() {
        return super.getValue();
    }

    @Override
    public void setValue(String value) {
        super.setValue(value);
    }

}
